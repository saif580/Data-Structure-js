//Adding an element at the end of array o(1)
let array=[1,2]
array.push(3)
console.log(array)

//Adding an element at the start of array o(n)
array.unshift(0)
console.log(array)


//removing an element from the starting
array.shift()
console.log(array)

//removing an element from the end
array.pop()
console.log(array)